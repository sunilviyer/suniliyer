/**
 * Get term by romanized name (case-insensitive)
 */
export function getTermByName(name: string): SanskritTerm | undefined {
  return sanskritGlossary.find(
    term => term.term.toLowerCase() === name.toLowerCase()
  );
}

/**
 * Search glossary by partial term or meaning
 */
export function searchGlossary(query: string): SanskritTerm[] {
  const lowerQuery = query.toLowerCase();
  return sanskritGlossary.filter(
    term =>
      term.term.toLowerCase().includes(lowerQuery) ||
      term.meaning.toLowerCase().includes(lowerQuery) ||
      term.constitutionalApplication.toLowerCase().includes(lowerQuery)
  );
}
